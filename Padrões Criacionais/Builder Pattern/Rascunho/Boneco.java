package Rascunho;

public class Boneco {
    private String head;
    private String tronco;
    private String bracos;
    private String pernas;

    public String getHead() {
        return head;
    }

    public String getTronco () {
        return tronco;
    }

    public String getBracos () {
        return bracos;
    }

    public String getPernas () {
        return pernas;
    }

    public void setHead (String head) {
        this.head = head;
    }

    public void setTronco(String tronco) {
        this.tronco = tronco;
    }

    public void setBracos(String bracos) {
        this.bracos = bracos;
    }

    public void setPernas(String pernas) {
        this.pernas = pernas;
    }
}
